   function renderCards(data, containerId, isLink = true) {
     const container = document.getElementById(containerId);
   
     data.forEach(({ title, description, url }) => {
       const card = document.createElement(isLink && url ? "a" : "div" );
   
       if (isLink && url) {
         card.href = url;
         card.target = "_blank";
         card.rel = "noopener noreferrer";
       }
   
       card.className =
         "bg-purple-100 border border-purple-200 rounded-2xl p-5 shadow-sm hover:shadow-md transition-all group";
   
       card.innerHTML = `
         <h3 class="text-lg font-semibold text-purple-700 group-hover:text-purple-600 mb-2">${title}</h3>
         <p class="text-sm text-gray-700">${description}</p>
       `;
   
       container.appendChild(card);
     });
   }
   
